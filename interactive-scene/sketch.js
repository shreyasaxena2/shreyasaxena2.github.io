let diam = 30


function setup() {
  createCanvas(windowWidth, windowHeight);
}

function draw() {
  background(220);
  ball()

}


function ball() {
  if (((mouseX <= windowWidth) && (mouseY === 0)) || ((mouseX === 0) && (mouseY <= windowHeight))) {
      diam = diam + 2
      circle(mouseX, mouseY, diam)
  }
  else {
    circle(mouseX, mouseY, diam)    
  }

  
}